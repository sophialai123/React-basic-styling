function add(num1 , num2){
    return num1 + num2;

}


function subtract(num1 , num2){
    return num1 - num2;

}


function mutiple(num1 , num2){
    return num1 * num2;

}


function divide(num1 , num2){
    return num1 / num2;

}


// export all functions
export {add, mutiple, subtract, divide};